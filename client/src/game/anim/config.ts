// Единый конфиг анимаций комнаты — «скорость и приоритет важности» в одном месте.
// Это осознанно единственный источник правды для тайминга/физики: движок, хореография
// и CardBody читают отсюда, чтобы фил карт настраивался в одном файле, а не по всему коду.

export interface SpringConfig {
  /** жёсткость — как сильно карту тянет к цели (больше = резче/быстрее) */
  stiffness: number;
  /** затухание — гасит колебания (меньше критического = лёгкий овершут/«баунс») */
  damping: number;
}

export const anim = {
  // Пружины по каналам. Позиция чуть «мягче» (заметный полёт), угол/масштаб — жёстче,
  // чтобы не «желировать» бесконечно. damping < 2*sqrt(stiffness) → лёгкий баунс = juice.
  posSpring: { stiffness: 120, damping: 14 } as SpringConfig,
  rotSpring: { stiffness: 150, damping: 16 } as SpringConfig,
  scaleSpring: { stiffness: 170, damping: 20 } as SpringConfig,

  // Наклон карты от скорости движения — «инерция»: карта кренится в сторону полёта,
  // как в Balatro (rotation-lag). Радиан на пиксель/сек горизонтальной скорости.
  tilt: {
    factor: 0.0007,
    max: 0.3, // ~17°
  },

  // Максимальный шаг интегрирования: при лаге вкладки dt не должен «взрывать» пружину.
  maxStepSec: 1 / 30,

  // Толщина стопки: вертикальное смещение на карту в покое (даёт 3D-ощущение колоды).
  deck: { stackDy: 0.4 },

  // Хореография растасовки, секунды. Фазы идут подряд: подъём → риффл → оседание.
  shuffle: {
    lift: { dur: 0.28, height: 46 }, // стопка приподнимается над столом
    riffle: { dur: 0.5, spread: 64, arch: 34, lean: 0.16 }, // две половины расходятся дугой, кренятся и складываются
    settle: { dur: 0.42, jitter: 0.05 }, // оседание в ровную стопку, лёгкий разброс углов (рад)
  },

  // Приоритеты анимаций — задел, чтобы более важная вытесняла менее важную
  // (раздача важнее растасовки, обе важнее idle). Пока просто числа-константы.
  priority: {
    idle: 0,
    shuffle: 10,
    deal: 20,
  },
} as const;

export type AnimConfig = typeof anim;
