// Кто на столе отбрасывает тень и какую. Чистые правила: движок по ним рисует, а считать
// «сколько теней и где» удобнее и проверяемее здесь.
//
// Главное правило: ТЕНИ НЕ СКЛАДЫВАЮТ АЛЬФУ. Полупрозрачные фигуры, наезжая друг на
// друга, темнеют на каждом наложении — под плотным веером выходила грязная полоса вместо
// тени. Лечится это не непрозрачностью, а порядком рисования: силуэты теней собираются в
// МАСКУ (их объединение), и сквозь неё один раз заливается полупрозрачный прямоугольник.
// Сколько бы теней ни пересеклось, заливка ложится ровно один раз — стандартный приём
// «merged shadows», он же используется для мягких теней в 2D-движках.

export interface ShadowCaster {
  /** Позиция карты на экране. */
  x: number;
  y: number;
  rot: number;
  /** Масштаб самой карты (тень повторяет его). */
  scale: number;
  /** Насколько карта поднята над столом: 0 — лежит, больше — тень уходит дальше. */
  lift: number;
  /** Слой: тень всегда чуть ниже своего хозяина. */
  z: number;
}

export interface FanShadowInput {
  /** Экранные x карт веера, по порядку. */
  xs: readonly number[];
  /** Ширина карты — по ней меряем, не слиплись ли соседние тени. */
  cardW: number;
  /** Индекс карты, которую сейчас тащат: у неё своя тень, в вееере она не нужна. */
  skip?: number;
}

/**
 * Какие карты веера отбрасывают тень.
 *
 * Под каждой картой тесного веера тень не нужна: их края почти совпадают, и вместе они
 * дают сплошную полосу. Берём каждую, что отошла от предыдущей хотя бы на треть карты,
 * плюс верхнюю — она лежит поверх всех и её тень видно целиком.
 */
export function fanShadowIndices(o: FanShadowInput): number[] {
  const gap = o.cardW * 0.33;
  const out: number[] = [];
  let lastX = Number.NEGATIVE_INFINITY;
  for (let i = 0; i < o.xs.length; i++) {
    if (i === o.skip) continue;
    const isTop = i === o.xs.length - 1;
    if (!isTop && Math.abs(o.xs[i]! - lastX) < gap) continue;
    out.push(i);
    lastX = o.xs[i]!;
  }
  return out;
}

/**
 * Силуэт тени карты — восьмиугольник: прямоугольник со срезанными углами. Точные скругления
 * маске не нужны (на экране это пара пикселей), а восемь точек рисуются вчетверо дешевле
 * дуг и дают ту же читаемую форму.
 *
 * Возвращает плоский список координат [x0,y0, x1,y1, …] в экранных координатах — именно в
 * таком виде его принимает Graphics.poly().
 */
export function shadowSilhouette(o: {
  x: number;
  y: number;
  w: number;
  h: number;
  rot: number;
}): number[] {
  const hw = o.w / 2;
  const hh = o.h / 2;
  const c = Math.min(hw, hh) * CORNER_CUT;
  // Обход по часовой от левого верхнего среза.
  const local: Array<[number, number]> = [
    [-hw + c, -hh],
    [hw - c, -hh],
    [hw, -hh + c],
    [hw, hh - c],
    [hw - c, hh],
    [-hw + c, hh],
    [-hw, hh - c],
    [-hw, -hh + c],
  ];
  const cos = Math.cos(o.rot);
  const sin = Math.sin(o.rot);
  const out: number[] = [];
  for (const [lx, ly] of local) {
    out.push(o.x + lx * cos - ly * sin, o.y + lx * sin + ly * cos);
  }
  return out;
}

/** Доля меньшей полуоси, которую срезаем на углу. */
const CORNER_CUT = 0.22;

/**
 * Приподнятость карты в руке у игрока. Считается от ЭТАЛОНА (карта закрытой руки): чем
 * крупнее стала карта, тем выше она «над столом» и тем дальше её тень.
 */
export function liftOf(scale: number): number {
  return Math.max(0, scale - 1);
}
