import type { Offset } from "./deckStack";

// Как выглядит КУЧКА В ИГРАЛЬНОЙ ЗОНЕ.
//
// Стопки колоды и сброса разъезжаются на доли пикселя (deckStack.stackOffset): для колоды
// это верно — она про толщину пачки, а не про то, что в ней лежит. Кучке зоны нужно
// обратное: по ней должно быть видно, СКОЛЬКО в ней карт и ЧТО лежит на самом дне.
// Поэтому у неё своя геометрия, а не общий stackOffset с другим множителем.
//
// Три правила:
//   1) задние карты торчат из-под передней — кучка перестаёт выглядеть одинокой картой;
//   2) вширь кучка не растёт больше чем в 1.2 раза: сетка зоны и так тесная;
//   3) САМАЯ НИЖНЯЯ карта выпирает в правый нижний угол настолько, чтобы читался её
//      угловой индекс — по кучке видно, что у неё на дне, без раскрытия веера.
//
// Числа в долях КАРТЫ, а не в пикселях: кучка живёт в сетке, где размер карты плавающий
// (см. playGrid), и пиксельные константы разъехались бы с ней на первом же сжатии.

/** Разъезд по горизонтали от верхней карты к нижней. 0.2 = ровно те самые «не шире 1.2». */
export const PLAY_SPREAD_X = 0.2;

/**
 * Разъезд по вертикали. Больше горизонтального, и это не произвол: угловой индекс лица
 * (ранг+масть) сидит в правом нижнем углу текстуры и занимает нижнюю треть её высоты
 * (см. makeCardFaceTexture). Меньший разъезд открывал бы половину знака — «что-то торчит»
 * вместо «вижу, что внизу дама».
 */
export const PLAY_SPREAD_Y = 0.34;

/**
 * Какую долю разъезда делят между собой СРЕДНИЕ карты. Остальное достаётся нижней одним
 * скачком: именно этот разрыв и делает её отдельной вещью, а не просто самой дальней в
 * ряду одинаковых полосок.
 */
const INNER = 0.45;

/** Сколько места занимает кучка целиком, в долях карты. По нему сетка отводит ей ячейку. */
export const PLAY_STACK_FOOTPRINT = { w: 1 + PLAY_SPREAD_X, h: 1 + PLAY_SPREAD_Y };

/**
 * Смещение i-й карты кучки относительно центра ячейки, В ДОЛЯХ карты.
 *
 * i = 0 — самая нижняя (выпирает вправо-вниз), i = count-1 — верхняя (лежит выше-левее).
 * Кучка отцентрована по своему габариту: середина разъезда приходится на центр ячейки,
 * иначе кучка уползала бы из ячейки тем сильнее, чем больше в ней карт.
 */
export function playStackOffset(i: number, count: number): Offset {
  const n = Math.max(1, count);
  if (n === 1) return { dx: 0, dy: 0 };
  const depth = depthFraction(i, n);
  // Центрируем: верхняя карта уходит на пол-разъезда вверх-влево, нижняя — вниз-вправо.
  return {
    dx: (depth - 0.5) * PLAY_SPREAD_X,
    dy: (depth - 0.5) * PLAY_SPREAD_Y,
  };
}

/** Насколько глубоко лежит карта: 0 — верхняя, 1 — самая нижняя. */
function depthFraction(i: number, n: number): number {
  if (i <= 0) return 1; // дно всегда на полном выносе — ради него всё и затевалось
  if (i >= n - 1) return 0; // верхняя лежит в начале отсчёта
  // Средние делят внутреннюю долю: чем глубже, тем дальше, но все они остаются заметно
  // ближе к верхней, чем дно.
  const middles = n - 2;
  return (INNER * (n - 1 - i)) / middles;
}
