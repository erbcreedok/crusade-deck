import { describe, it, expect } from "vitest";
import { moveCard } from "./deckOrder";

const deck = ["AвЩ†", "2вЩ†", "3вЩ†", "4вЩ†", "5вЩ†"];

describe("moveCard", () => {
  it("–і–≤–Є–≥–∞–µ—В –Ї–∞—А—В—Г –≤–њ–µ—А—С–і –њ–Њ –Ї–Њ–ї–Њ–і–µ", () => {
    expect(moveCard(deck, "AвЩ†", 2)).toEqual(["2вЩ†", "3вЩ†", "AвЩ†", "4вЩ†", "5вЩ†"]);
  });

  it("–і–≤–Є–≥–∞–µ—В –Ї–∞—А—В—Г –љ–∞–Ј–∞–і –њ–Њ –Ї–Њ–ї–Њ–і–µ", () => {
    expect(moveCard(deck, "5вЩ†", 1)).toEqual(["AвЩ†", "5вЩ†", "2вЩ†", "3вЩ†", "4вЩ†"]);
  });

  it("–љ–∞ —Б–≤–Њ—С –ґ–µ –Љ–µ—Б—В–Њ вАФ –њ–Њ—А—П–і–Њ–Ї –љ–µ –Љ–µ–љ—П–µ—В—Б—П", () => {
    expect(moveCard(deck, "3вЩ†", 2)).toEqual(deck);
  });

  it("–Є–љ–і–µ–Ї—Б –Ј–∞ –≥—А–∞–љ–Є—Ж–∞–Љ–Є вАФ –њ—А–Є–ґ–Є–Љ–∞–µ—В—Б—П –Ї –Ї—А–∞—О", () => {
    expect(moveCard(deck, "AвЩ†", 99)).toEqual(["2вЩ†", "3вЩ†", "4вЩ†", "5вЩ†", "AвЩ†"]);
    expect(moveCard(deck, "5вЩ†", -3)).toEqual(["5вЩ†", "AвЩ†", "2вЩ†", "3вЩ†", "4вЩ†"]);
  });

  it("–Ї–∞—А—В—Л –љ–µ—В –≤ –Ї–Њ–ї–Њ–і–µ вАФ –Ї–Њ–њ–Є—П –±–µ–Ј –Є–Ј–Љ–µ–љ–µ–љ–Є–є", () => {
    expect(moveCard(deck, "KвЩ¶", 0)).toEqual(deck);
  });

  it("–Є—Б—Е–Њ–і–љ—Л–є –Љ–∞—Б—Б–Є–≤ –љ–µ –Љ—Г—В–Є—А—Г–µ—В—Б—П", () => {
    const src = [...deck];
    moveCard(src, "AвЩ†", 3);
    expect(src).toEqual(deck);
  });
});
