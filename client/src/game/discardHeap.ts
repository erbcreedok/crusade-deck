// Как лежит СБРОС в покое: не стопкой, а небрежной горочкой.
//
// Сброс — это «сюда бросили», и выглядеть он должен соответственно: кучка карт внахлёст,
// каждая под своим углом. Ровная стопка читается как колода, то есть как то, откуда
// БЕРУТ, — а из сброса в покое ничего не берут, его сначала раскрывают веером (тап).
//
// Узор ФИКСИРОВАННЫЙ, а не случайный. Случайный пришлось бы где-то хранить (иначе кучка
// перекладывалась бы на каждой перерисовке) и синхронизировать между игроками, чтобы у
// всех она выглядела одинаково. Готовая раскладка на семь карт решает обе задачи даром.
//
// Семь — потому что дальше кучка перестаёт читаться: карты кладутся в круг радиусом около
// одной восьмой карты, и восьмая уже ничего не добавляет к силуэту. Остальные считаются
// лежащими ПОД видимыми — их и не рисуем, сколько бы их ни было (счётчик под слотом
// показывает настоящее число).

import type { Offset } from "./deckStack";

/** Сколько карт кучки видно. Остальные «под ними». */
export const DISCARD_HEAP_MAX = 7;

export interface HeapPose extends Offset {
  /** Поворот карты, в градусах. Именно он и делает кучку кучкой, а не смещения. */
  deg: number;
}

/**
 * Узор на семь карт: смещения в долях КАРТЫ, повороты в градусах.
 *
 * Порядок — от ВЕРХНЕЙ карты вглубь: 0 достаётся самой верхней. Поэтому свежесброшенная
 * карта ложится в середину горки, а не с краю, — это и читается как «бросили сюда».
 *
 * Смещения держатся в пределах ±0.12 карты: слот сброса шире карты ровно на четверть
 * (SLOT_PAD в layout.ts), и кучка обязана в него уместиться, иначе она свесится за
 * размеченный бокс.
 */
const HEAP: readonly HeapPose[] = [
  { dx: 0.0, dy: 0.0, deg: -3 },
  { dx: 0.11, dy: -0.05, deg: 9 },
  { dx: -0.09, dy: 0.06, deg: -11 },
  { dx: 0.04, dy: 0.12, deg: 5 },
  { dx: -0.12, dy: -0.07, deg: 14 },
  { dx: 0.1, dy: 0.08, deg: -8 },
  { dx: -0.03, dy: -0.12, deg: 12 },
];

/**
 * Поза карты, лежащей на `depth` карт от верха кучки (0 — верхняя).
 *
 * Глубже видимой части поза не меняется: такие карты всё равно не рисуются, а держать их
 * в центре кучки нужно, чтобы всплывшая карта не прилетала в кадр откуда-то со стороны.
 */
export function discardHeapPose(depth: number): HeapPose {
  const d = Math.max(0, Math.floor(depth));
  return HEAP[Math.min(d, HEAP.length - 1)]!;
}

/** Видна ли карта такой глубины: у горки на виду только верхние DISCARD_HEAP_MAX. */
export function discardHeapVisible(depth: number): boolean {
  return depth >= 0 && depth < DISCARD_HEAP_MAX;
}

/**
 * Габарит кучки в долях карты — по нему движок ставит счётчик под слотом.
 *
 * Постоянный, а не «по числу карт»: у стопки толщина растёт с каждой картой, а у горки
 * силуэт замирает на седьмой. Счётчик, привязанный к растущему габариту, полз бы вниз до
 * тридцать шестой карты, хотя сама кучка давно не меняется.
 */
export function discardHeapExtent(): { w: number; h: number } {
  const xs = HEAP.map((p) => p.dx);
  const ys = HEAP.map((p) => p.dy);
  return { w: Math.max(...xs) - Math.min(...xs), h: Math.max(...ys) - Math.min(...ys) };
}
