// Раскладка «пипсов» (значков масти) по номиналу — как на классических числовых картах:
// столько значков, сколько номинал. Верхняя половина карты стоит прямо, нижняя —
// перевёрнута на 180°, ровно как на настоящей карте. Чистая геометрия в долях 0..1
// (0 — левый/верхний край лица, 1 — правый/нижний), рисует по ней движок (cardTextures).

export interface Pip {
  x: number; // 0..1 по ширине лица
  y: number; // 0..1 по высоте лица
  flip: boolean; // значок в нижней половине печатается вверх ногами
}

// Три столбца значков. Крайние сдвинуты внутрь от углов — там сидят угловые ранг+масть,
// и нужен воздух по бокам, иначе пипсы липнут к краю (и к белой кайме рубашки на обороте).
const L = 0.34;
const C = 0.5;
const R = 0.66;

// Каноническая покерная раскладка: пары [столбец, доля высоты]. Число пар = номинал.
const TABLE: Record<number, [number, number][]> = {
  2: [[C, 0.18], [C, 0.82]],
  3: [[C, 0.18], [C, 0.5], [C, 0.82]],
  4: [[L, 0.18], [R, 0.18], [L, 0.82], [R, 0.82]],
  5: [[L, 0.18], [R, 0.18], [C, 0.5], [L, 0.82], [R, 0.82]],
  6: [[L, 0.18], [R, 0.18], [L, 0.5], [R, 0.5], [L, 0.82], [R, 0.82]],
  7: [[L, 0.18], [R, 0.18], [C, 0.34], [L, 0.5], [R, 0.5], [L, 0.82], [R, 0.82]],
  8: [[L, 0.18], [R, 0.18], [C, 0.34], [L, 0.5], [R, 0.5], [C, 0.66], [L, 0.82], [R, 0.82]],
  9: [[L, 0.18], [R, 0.18], [L, 0.395], [R, 0.395], [C, 0.5], [L, 0.605], [R, 0.605], [L, 0.82], [R, 0.82]],
  10: [[L, 0.18], [R, 0.18], [C, 0.28], [L, 0.395], [R, 0.395], [L, 0.605], [R, 0.605], [C, 0.72], [L, 0.82], [R, 0.82]],
};

// Сжатие вертикали к центру: воздух сверху и снизу, чтобы крайние ряды не липли к краю
// (и к белой кайме на обороте). 1 — без отступа; 0.875 ≈ по 4% поля сверху и снизу.
const VSQUEEZE = 0.875;

/**
 * Пипсы для числового номинала «2».."10". Для картинок (J/Q/K) и туза раскладки нет —
 * их движок рисует по-своему (буква/крупная масть), поэтому здесь возвращается пустой
 * список: у вызывающей стороны один путь «нет пипсов → рисуй иначе».
 */
export function pipLayout(rank: string): Pip[] {
  const n = Number(rank);
  const cells = Number.isInteger(n) ? TABLE[n] : undefined;
  if (!cells) return [];
  return cells.map(([x, y]) => {
    const py = 0.5 + (y - 0.5) * VSQUEEZE;
    return { x, y: py, flip: py > 0.5 };
  });
}
