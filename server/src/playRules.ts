// Правила ИГРАЛЬНОЙ ЗОНЫ — среднего бокса стола, куда игроки выкладывают карты из рук.
// Чистая логика, состояние меняет обработчик сообщений.
//
// Зона — это СПИСОК МАССИВОВ карт, а не один массив: на столе лежит несколько отдельных
// кучек, и каждая может быть от одной карты до сколь угодно большой. Раскладывать их по
// экрану — забота клиента (сетка считается по индексу), сервер про геометрию не знает
// ничего и хранит только «что в какой кучке лежит».
//
// Верх кучки — ПОСЛЕДНИЙ элемент массива: та же договорённость, что у колоды, сброса и
// клиентского topCard.ts. Разнобой здесь стоил бы карты, кладущейся не тем концом.
//
// Правил игры тут по-прежнему нет: кто угодно может положить что угодно куда угодно и
// забрать обратно любую карту. Очерёдность (queue lock) появится отдельным слоем.

export type PlayStacks = readonly (readonly string[])[];

function copy(stacks: PlayStacks): string[][] {
  return stacks.map((s) => [...s]);
}

/**
 * Выложить карту из руки в зону.
 *
 * `stackIndex` — в какую кучку класть. Нет индекса (или он не указывает на существующую
 * кучку) — карта ложится НОВЫМ массивом в конец: так дроп в пустое место создаёт кучку, а
 * дроп на карту доливает в её кучку.
 *
 * Про «не указывает на существующую»: индекс приходит от клиента и мог устареть, пока
 * карта летела с пальца (кто-то успел смахнуть зону в сброс). Ронять действие в null
 * из-за этого нельзя — карта уже уехала из руки визуально, и игрок увидел бы, как она
 * необъяснимо отскакивает. Дешевле и честнее положить её отдельной кучкой.
 */
export function playCard(
  hand: readonly string[],
  stacks: PlayStacks,
  card: string,
  stackIndex?: number,
): { hand: string[]; stacks: string[][] } | null {
  if (!hand.includes(card)) return null;
  const next = copy(stacks);
  if (stackIndex !== undefined && stackIndex >= 0 && stackIndex < next.length) {
    next[stackIndex].push(card);
  } else {
    next.push([card]);
  }
  return { hand: hand.filter((c) => c !== card), stacks: next };
}

/**
 * Забрать карту из зоны себе в руку. Карта ищется ПО ИМЕНИ, а не по позиции: в зоне всё
 * лежит лицом вверх и видно всем, так что игрок берёт ровно то, на что смотрит — слепоты,
 * ради которой колода и сброс работают по индексу, здесь нет.
 *
 * Копаться в середине кучки разрешено: пока правил игры нет, «отбитая» карта ничем не
 * отличается от любой другой.
 *
 * Опустевшая кучка ИСЧЕЗАЕТ из списка. Иначе сетка копила бы пустые ячейки — место
 * занимают, показать нечего.
 */
export function takeFromPlay(stacks: PlayStacks, card: string): { stacks: string[][]; card: string } | null {
  const at = stacks.findIndex((s) => s.includes(card));
  if (at < 0) return null;
  const next = copy(stacks);
  next[at] = next[at].filter((c) => c !== card);
  if (next[at].length === 0) next.splice(at, 1);
  return { stacks: next, card };
}

/**
 * «В СБРОС»: вся зона уезжает в сброс одним движением. Порядок — по кучкам слева направо,
 * внутри кучки снизу вверх, чтобы верхняя карта зоны осталась верхней и в сбросе.
 */
export function clearPlay(stacks: PlayStacks, discard: readonly string[]): string[] {
  return [...discard, ...playCards(stacks)];
}

/** Все карты зоны плоским списком — нужен перераздаче, она сгребает в колоду и её тоже. */
export function playCards(stacks: PlayStacks): string[] {
  return stacks.flatMap((s) => [...s]);
}
